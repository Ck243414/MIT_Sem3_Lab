class PatternB
{
    public static void main(String args[])
    {
                int a[] = {1,2,3,4,5};
                for (int i : a)
                {
                    for(int j=1;j<=i;j++)
                    {
                        System.out.print(i+ " ");
                    }
                    System.out.println();
                }
    }     
}
        
    
